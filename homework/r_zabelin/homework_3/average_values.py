# This app takes two numbers and calculates their arithmetic mean and geometric mean.

# Block 1: testing integer numbers

first_num1 = 5

second_num1 = 3

print("Block 1: int")

print("Arithmetic mean:", (first_num1 + second_num1) / 2)

print("Geometric mean:", (first_num1 * second_num1) ** 0.5)

# Block 2: testing float numbers

first_num2 = 2.5

second_num2 = 4

print("Block 2: float")

print("Arithmetic mean:", (first_num2 + second_num2) / 2)

print("Geometric mean:", (first_num2 * second_num2) ** 0.5)

# Block 3: testing negative numbers

first_num3 = -7

second_num3 = 2.3

print("Block 3: negative num")

print("Arithmetic mean:", (first_num3 + second_num3) / 2)

print("Geometric mean:", (first_num3 * second_num3) ** 0.5)

# Block 4: testing zero

first_num4 = 0

second_num4 = 3.14

print("Block 4: zero")

print("Arithmetic mean:", (first_num4 + second_num4) / 2)

print("Geometric mean:", (first_num4 * second_num4) ** 0.5)
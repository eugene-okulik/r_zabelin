# This app takes two numbers and calculates their sum, difference, and product.

# Block 1: testing integer numbers

first_num1 = 5

second_num1 = 3

print("Block 1: int")

print("sum=", first_num1 + second_num1)

print("difference=", first_num1 - second_num1)

print("product=", first_num1 * second_num1)

# Block 2: testing float numbers

first_num2 = 2.5

second_num2 = 4

print("Block 2: float")

print("sum=", first_num2 + second_num2)

print("difference=", first_num2 - second_num2)

print("product=", first_num2 * second_num2)

# Block 3: testing negative numbers

first_num3 = -7

second_num3 = 2.3

print("Block 3: negative num")

print("sum=", first_num3 + second_num3)

print("difference=", first_num3 - second_num3)

print("product=", first_num3 * second_num3)

# Block 4: testing zero

first_num4 = 0

second_num4 = 3.14

print("Block 4: zero")

print("sum=", first_num4 + second_num4)

print("difference=", first_num4 - second_num4)

print("product=", first_num4 * second_num4)